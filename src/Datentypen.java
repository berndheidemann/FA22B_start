


public class Datentypen {

    public static void main(String[] args) {

        System.out.println("Hallo Welt");

        // Variablen

        // *************
        // PRIMITIVE DATENTYPEN

        // datentyp variablenname = initialwert;

        // Ganzzahl, 8 Bit
        byte by=12;

        // Ganzahl, 16 Bits
        short sh=1234;

        // Ganzahl, kann von - 2^31 bis +2^31-1 --> 32Bits
        int i = 0;

        System.out.println(i);
        i = 23;
        System.out.println(i);

        // Ganzzahl, 64bits
        long l = 12307213981732123L;
        System.out.println(l);

        // Fließkommazahl, 64bits
        double d = 1321341.1234123213213123213;
        System.out.println(d);

        // Fließkommazahl, 32 bits
        float f = 123.14131f;
        System.out.println(f);

        // Wahrheitswert, 1 Bit
        boolean b = true;
        System.out.println(b);

        // Digit, bits?
        char c='i';

        // **********
        // String --> Objekt
        String s="Hallo Welt";











    }
}
